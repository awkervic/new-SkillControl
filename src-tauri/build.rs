use std::fs;
use std::path::Path;

fn main() {
    let icons_dir = Path::new("icons");
    if !icons_dir.exists() {
        let _ = fs::create_dir_all(icons_dir);
    }
    
    let ico_path = icons_dir.join("icon.ico");
    
    if !ico_path.exists() {
        // 1x1 pixel valid legacy DIB ICO file bytes (70 bytes total)
        // Accepted by Windows Resource Compiler (RC.EXE)
        let ico_bytes: &[u8] = &[
            // ICO Header (6 bytes)
            0x00, 0x00, 0x01, 0x00, 0x01, 0x00,
            // Directory Entry (16 bytes)
            0x01, 0x01, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x30, 0x00, 0x00, 0x00, 0x16, 0x00, 0x00, 0x00,
            // BMP DIB Header (40 bytes)
            0x28, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            // Pixel Data (4 bytes, ARGB: Blue)
            0xff, 0x00, 0x00, 0xff,
            // AND Mask (4 bytes)
            0x00, 0x00, 0x00, 0x00,
        ];
        
        let _ = fs::write(&ico_path, ico_bytes);
    }
    
    tauri_build::build()
}
